# At this point the test switches to the Skeleton page, which
# causes a delayed update of the page and a sensitization calculation,
# which has a sensitization checkpoint.  The test then immediately
# switches to another page.  The problem is that if the test is
# *recorded* too quickly, the sensitization checkpoint can appear in
# the log file after the page has been switched.  In that case, when
# the test is replayed, sometimes the page is updated after the
# switch. Because the update is slow (homogeneity is calculated) it's
# not done if the page isn't current. So if the page switch occurs too
# soon, the update isn't done and the sensitization call isn't made,
# and the test hangs on the checkpoint.  The solution is to move the
# checkpoint so that it occurs before the page switch in the log
# file. This isn't a good solution because if this file is ever
# re-recorded, the checkpoint might move back and the problem will
# reappear.
